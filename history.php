                        <h1><span>SEARCH FOR THE RIGHT VISION</span> </h1>
                        <p class="PT-3">To hold the torch to forward the child's rights movement, however, there was a complete dearth of appropriate philosophy and vision. Children, the neglected majority, did not have much standing on the adult dominated society. The welfare and service for the children were merely viewed as a charity and had conservative approaches. The students wanted to change that perception owing to their knowledge of the international declarations and convention on the rights of the child which recognised the Child as an integral component of the society with their inherent rights to survival, well being, protection and freedom. Children are to be nurtured with the best the mankind has to offer and need special treatment because of their vulnerable disposition. The conservative approach at dealing with the children's welfare was unproductive and harmful. They wanted a progressive approach.
