                  <p>It is a fine spring day in 1986, a group of young university students are discussing their future, sitting on a sprawling garden of the Tribhuvan University compound. The students having a varied academic background, but one thing
                     in common - all of them are student activists. As educated and aware citizens, they have shown their dedication to human rights, development and social service of the country by being part of the human rights and pro-democracy
                     movements in the eighties. Now in the threshold of their new life out of the university, they want to do something really worthwhile and important for their people, society and the nation. The discussion focuses on one young man
                     who relates his experience about an international seminar he had an opportunity to join. The seminar taking place in Bangkok, Thailand dealt with the issue of child labour and was organised by a recently established regional level
                     organisation called Child Workers in Asia (CWA). The young man talks excitedly of his experience, how the issues raised during the seminar touched him and how it made him realise the gravity of the problem of child labour in his
                     own country. It was strange that child labour is not the 'hot' issue at that point of time. Leave child labour, even child rights is almost an unheard term, even though human rights and women rights, even trade union movements
                     are getting momentum. It is appalling how everybody is oblivious to the serious problems and threats faced by the children such as high rate of child mortality, abuse and exploitation of children, the involvement of children in
                     the exploitative and health-hazardous labour field, the existence of bonded child labour, trafficking of young girls, etc. The students decide there and then. They will raise the issue of child labour and child rights for the social
                     emancipation of children from all form of exploitation and for the integration of child's rights into the broader social movement for freedom, peace and equality. For development. For a better life of a new generation. They decided
                     to start a concerned group to help protect children living and working in the most difficult circumstances.
                  </p>
