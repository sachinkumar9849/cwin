                     <h1>SEARCH FOR THE RIGHT VISION</h1>
                  </div>
                  <p>To hold the torch to forward the child's rights movement, however, there was a complete dearth of appropriate philosophy and vision. Children, the neglected majority, did not have much standing on the adult dominated society. The welfare and service for the children were merely viewed as a charity and had conservative approaches. The students wanted to change that perception owing to their knowledge of the international declarations and convention on the rights of the child which recognised the Child as an integral component of the society with their inherent rights to survival, well being, protection and freedom. Children are to be nurtured with the best the mankind has to offer and need special treatment because of their vulnerable disposition. The conservative approach at dealing with the children's welfare was unproductive and harmful. They wanted a progressive approach.
                  </p>
                  <p>So the initiators discussed, debated and hold research studies on the status and problems of children. After a year-long deliberation, they developed the basic vision, philosophy, strategies and plans of action to their new initiative to help protect children from risk and to build up a social movement dedicated to children's rights and against all forms of servitude of children. Formally established as an organisation on January 1, 1987, they named it "Child Workers in Nepal Concerned Center (CWIN)". CWIN became the first child rights organisation in Nepal.</p>
               </div>
               <div class="twelve text-left">
                  <div class="title_border mb-4 mt-5">
                     <h1>REGISTRATION</h1>
                  </div>
                  <p>Even though formally established in 1987, CWIN was not officially registered for a couple of years for multiple reasons. First, the political climate was not favourable at the time of CWIN's establishment. The social service scenario, largely viewed as the charity service, was monopolised. Every organisation and institution was under the umbrella of the then SSNCC. CWIN, with its founder members with student background and its fiercely independent policy and vision at the social service for children, was not the best organisation to earn the goodwill of the SSNCC, the tool of the anarchist and totalitarian one-party government system of Panchayat. Then the much-despised political order gave way to the people's movement for the restoration of democracy in 1991 and the subsequent formation of the first democratic government in the country. Soon the SSNCC also had a new set up and CWIN was officially registered under both SWC and Chief District Office (CDO) in 1991 as child's rights activist and advocate organisation.
                  </p>
