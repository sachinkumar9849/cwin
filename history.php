                    <p>So the initiators discussed, debated and hold research studies on the status and problems of children. After a year-long deliberation, they developed the basic vision, philosophy, strategies and plans of action to their new initiative to help protect children from risk and to build up a social movement dedicated to children's rights and against all forms of servitude of children. Formally established as an organisation on January 1, 1987, they named it "Child Workers in Nepal Concerned Center (CWIN)". CWIN became the first child rights organisation in Nepal.</p>
